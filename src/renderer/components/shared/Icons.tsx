interface IconProps {
  size?: number
  className?: string
}

const defaults = { size: 24 }

function icon(d: string | string[], fill?: boolean) {
  return function Icon({ size = defaults.size, className }: IconProps) {
    const paths = Array.isArray(d) ? d : [d]
    return (
      <svg
        width={size}
        height={size}
        viewBox="0 0 24 24"
        fill={fill ? 'currentColor' : 'none'}
        stroke={fill ? 'none' : 'currentColor'}
        strokeWidth="2"
        strokeLinecap="round"
        strokeLinejoin="round"
        className={className}
      >
        {paths.map((p, i) => <path key={i} d={p} />)}
      </svg>
    )
  }
}

